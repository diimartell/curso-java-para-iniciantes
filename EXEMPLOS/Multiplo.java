package EXEMPLOS;

public class Multiplo {
    public static void main(String[] args) {
        // Verificar se um número é múltiplo de 3.
        int num = 9;

        if (num % 3 == 0) {
            System.out.println(num + " é múltiplo de 3.");
        } else {
            System.out.println(num + " não é múltiplo de 3.");
        }

        
    }
}
